import 'server-only';
/**
 * lib/demo/product-catalogue.ts — 100 real Indian FMCG products.
 *
 * Every product has:
 *   - Real EAN-13 barcode (scannable / lookable)
 *   - Open Food Facts CDN image URL
 *   - Category, brand, MRP, weight/volume
 *   - Realistic demo inventory/expiry data
 *
 * Used by scan demo, inventory, expiry, audit, and GRN demo datasets.
 * Server-only — never bundled to the client.
 */

export interface CatalogueProduct {
  ean: string;
  name: string;
  brand: string;
  category: string;
  mrp: number;           // ₹ MRP
  weight: string;        // e.g. "100g", "500ml"
  /** Open Food Facts CDN URL — real product image */
  imageUrl: string | null;
  /** Which approved EAN list this product belongs to */
  eanList: string;
  /** Shelf life in days (approx) */
  shelfLifeDays: number;
}

// OFF image base: https://images.openfoodfacts.org/images/products/{ean}/front_en.3.400.jpg
// We use the direct product image URL pattern from Open Food Facts
function off(ean: string, suffix = '1.400.jpg'): string {
  // Format EAN for OFF URL (split into 3/3/3/1 for EAN-13)
  const p1 = ean.slice(0, 3);
  const p2 = ean.slice(3, 6);
  const p3 = ean.slice(6, 9);
  const p4 = ean.slice(9);
  return `https://images.openfoodfacts.org/images/products/${p1}/${p2}/${p3}/${p4}/front_en.${suffix}`;
}

