/** Get a product by exact EAN. Returns null if not in the catalogue. */
export function getProductByEan(ean: string): CatalogueProduct | null {
  return PRODUCT_CATALOGUE.find(p => p.ean === ean) ?? null;
}

/** Get products by category. */
export function getProductsByCategory(category: string): CatalogueProduct[] {
  return PRODUCT_CATALOGUE.filter(p => p.category === category);
}

/** Get the first N products from the catalogue. */
export function getCatalogueSlice(n: number, offset = 0): CatalogueProduct[] {
  return PRODUCT_CATALOGUE.slice(offset, offset + n);
}
